    public boolean check() {
        if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "ko dc de trong");
            txtName.requestFocus();// đặt chuột vào txtName
            return false;//đúng
        } else {
            if (!txtName.getText().matches("[a-zA-Z]+")) {
                JOptionPane.showMessageDialog(this, "sai dinh dang");
                txtName.requestFocus();// đặt chuột vào txtName
                txtName.setText(null);//xóa đi
                return false;// đúng
            }
        }
        if (txtAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "ko dc de trong");
            txtAddress.requestFocus();// đặt chuột vào txtName
            return false;//đúng
        } else {
            if (!txtAddress.getText().matches("[a-zA-Z]+")) {
                JOptionPane.showMessageDialog(this, "sai dinh dang");
                txtAddress.requestFocus();// đặt chuột vào txtName
                txtAddress.setText(null);//xóa đi
                return false;// đúng
            }
        }
        
        return true;
    }

