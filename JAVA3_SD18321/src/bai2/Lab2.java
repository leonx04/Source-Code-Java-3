    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        if (check()) {
            String str= "";
            str = "Name:"+txtName.getText()+"\n";
            str = str + "address"+ txtAddress.getText()+"\n";
            if (rdoFemale.isSelected()) {
                str = str +"sex: vit"+"\n";
            } else {
                str = str +"sex: ga"+"\n";
            }//lay o combo
            str= str+cboQualification.getSelectedItem();
            //lay ơ checkbox
            if (chkDance.isSelected()) {
                str = str +"hobby: Dance"+"\n";
            }
            if (chkReading.isSelected()) {
                str = str +"hobby: Reading"+"\n";
            }
            if (chkSing.isSelected()) {
                str = str +"hobby: Sing"+"\n";
            }
            JOptionPane.showMessageDialog(this, str);
        }
    }//GEN-LAST:event_btnValidateActionPerformed

